package ru.katacademy.bank_app.frauddetection.account.domain.enumtype;

/**
 * Перечисление, представляющее возможные статусы банковского счета.
 *
 * <p>Используется для обозначения текущего состояния счета в банковской системе.
 * Определяет, доступен ли счет для операций или имеет ограничения.</p>
 *
 * @author Sheffy
 */
public enum AccountStatus {
    /**
     * Счет активен и доступен для всех операций.
     */
    ACTIVE,

    /**
     * Счет заблокирован. Операции по счету временно недоступны.
     */
    BLOCKED,

    /**
     * Счет закрыт. Дальнейшие операции невозможны.
     */
    CLOSE
}
